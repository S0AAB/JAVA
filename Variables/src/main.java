public static void main(String[] args) {
    // Tipos de datos primitivos
    byte smallNumber = 100;  // Un número entero pequeño de 8 bits
    short mediumNumber = 5000;  // Un número entero de 16 bits
    int age = 30;  // Un número entero de 32 bits
    long largeNumber = 100000L;  // Un número entero de 64 bits
    float pi = 3.14159f;  // Un número decimal de 32 bits
    double height = 1.75;  // Un número decimal de 64 bits
    char gender = 'M';  // Un único carácter
    boolean isStudent = true;  // Un valor lógico (booleano)
    String name = "John";  // Cadena de texto (no es primitivo, es un objeto)


}
